package advanced_practice.practice02;

public class Q01_Ascii_AdiniYaz {
    //Adinizin icerdigi harfleri ve ascii table kullanmadan, adinizi yazdiran bir kod yaziniz.
    public static void main(String[] args) {

        char ch1 = 'B'-1;//A
        char ch2 = 'm'-1;//l
        char ch3 = 'h'+1;//i
        System.out.println(""+ch1+ch2+ch3);//Alib
    }
}
